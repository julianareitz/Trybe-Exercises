//  O filter retorna num array todos os elementos que satisfação à condição, diferente do find que só retorna o primeiro elemento encontrado alterando o array.
// 1) Retorna um array de elementos encontrados que satisfação a condição;
// 2) Altera o array de origem;
// 3) Pode ser utilizando para remover um elemento de um array;

const numbers = [19, 21, 30, 3, 45, 22, 15];

const verifyEven = (number) => number % 2 === 0;

const isEven = numbers.filter(verifyEven);

console.log(isEven); // [ 30, 22 ]

// Outra forma de ser realizada sem a necessidade de criar uma nova função.
const isEven2 = numbers.filter((number) => number % 2 === 0);

console.log(isEven2); // [ 30, 22 ]


